    public void imprimirResultados(ResultSet rs, int pantalla){
        try {
            String res="";
            if (rs!=null){
                while (rs.next()) {
                    res = rs.getString(1)+ ", " 
                          +rs.getString(2)+ ", "
                          +rs.getString(3)+ ", "
                          +rs.getString(4)+ ", "
                          +rs.getString(5)+ ", "
                          +rs.getString(6)+ "\n";
                    listModel.addElement(res);
                }
            }else{
                res = "No se encontraron mascotas para este dueño" ;
            }
        } catch (SQLException ex) {
            Logger.getLogger(IngresoMascota.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void llenarComboBoxes(int i){
        raza.removeItemAt(2);
        raza.removeItemAt(1);
        raza.removeItemAt(0);
        if (i==0){
            raza.insertItemAt("Labrador", 0);
            raza.insertItemAt("Husky", 1);
            raza.insertItemAt("Chihuahua", 2);
        }
        else {
            raza.insertItemAt("Persa", 0);
            raza.insertItemAt("Siamés", 1);
            raza.insertItemAt("Snowshoe", 2);
        }
        
    }

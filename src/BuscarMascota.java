        ResultSet rs = bdd.enviarConsulta("select * from mascota where idmascota in "
                + "(select id_mascota from denuncia where zona = "+zona.getText()+" and tipo_denuncia = 1) "
                + "and id_raza = "+razas);
        imprimirResultados(rs,1);

